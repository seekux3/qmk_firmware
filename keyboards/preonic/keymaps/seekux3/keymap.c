bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  static bool haen_mods = false;
  static bool lshift_pressed = false;
  switch (keycode) {
        case QWERTY:
          if (record->event.pressed) {
            set_single_persistent_default_layer(_QWERTY);
          }
          return false;
          break;
        case COLEMAK:
          if (record->event.pressed) {
            set_single_persistent_default_layer(_COLEMAK);
          }
          return false;
          break;
        case DVORAK:
          if (record->event.pressed) {
            set_single_persistent_default_layer(_DVORAK);
          }
          return false;
          break;
        case LOWER:
          if (record->event.pressed) {
            layer_on(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          } else {
            layer_off(_LOWER);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          }
          return false;
          break;
        case RAISE:
          if (record->event.pressed) {
            layer_on(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          } else {
            layer_off(_RAISE);
            update_tri_layer(_LOWER, _RAISE, _ADJUST);
          }
          return false;
          break;
        case BACKLIT:
          if (record->event.pressed) {
            register_code(KC_RSFT);
            #ifdef BACKLIGHT_ENABLE
              backlight_step();
            #endif
            #ifdef __AVR__
            writePinLow(E6);
            #endif
          } else {
            unregister_code(KC_RSFT);
            #ifdef __AVR__
            writePinHigh(E6);
            #endif
          }
          return false;
          break;
#if 0
        // SHIFT+ESC키에 '~' 대체
        case KC_ESC:
          // 키보드가 눌렸을 경우
          if (record->event.pressed)
          {
            // 왼쪽 SHIFT키와 오른쪽 SHIFT키가 눌렸는지 확인
            if (get_mods() & MOD_MASK_SHIFT)
            {
              // SHIFT+ESC를 '~'키로 정의를 한다.
              register_code16(KC_TILD);
            }
            else
            {
              // ESC만 눌린 경우 ESC키로 정의한다.
              register_code(KC_ESC);
            }
          }
          // 키보드가 눌리지 않았을 경우 등록된 코드를 풀어준다.
          else {
            if (get_mods() & MOD_MASK_SHIFT)
            {
              unregister_code16(KC_TILD);
            } else {
              unregister_code(KC_ESC);
            }
          }
          return false; // 발생한 키 처리를 했으니 false 처리를 하여 중복으로 넘겨주지 않도록 한다.
#endif
        case KC_SPC:
          if (record->event.pressed)
          {
            if (get_mods() & MOD_MASK_SHIFT)
            {
              // SHIFT+SPACE을 한영키로 치환하는 것이기 때문에 SHIFT+SPACE가 되지 않게 SHIFT를 빼준다.
              if( get_mods() & MOD_BIT(KC_LSHIFT) )
                unregister_code(KC_LSHIFT);

              if( get_mods() & MOD_BIT(KC_RSHIFT) )
                unregister_code(KC_RSHIFT);

              register_code(KC_HAEN);
              haen_mods = true;
            } else {
              register_code(KC_SPC);
            }
          }
          else
          {
            if (haen_mods == true)
            {
              unregister_code(KC_HAEN);
              register_code(KC_LSHIFT);
              haen_mods = false;
            } else {
              unregister_code(KC_SPC);
            }
          }
          return false;

        case KC_LSHIFT:
          if (record->event.pressed)
          {
            lshift_pressed = true;
          } else {
            lshift_pressed = false;
          }
          break;

#if 0
        // SHIFT+DEL키에 INSERT키 대입
        case KC_DEL:
          // 키보드가 눌렸을 경우
          if (record->event.pressed)
          {
            // 왼쪽 SHIFT키와 오른쪽 SHIFT키가 눌렸는지 확인
            if (get_mods() & MOD_MASK_SHIFT)
            {
              // SHIFT+DEL을 INSERT키로 치환하는 것이기 때문에 SHIFT+INSERT가 되지 않게 SHIFT를 빼준다.
              if( get_mods() & MOD_BIT(KC_LSHIFT) )
                unregister_code(KC_LSHIFT); // Left SHIFT키가 눌렸을 경우 해제한다.

              if( get_mods() & MOD_BIT(KC_RSHIFT) )
                unregister_code(KC_RSHIFT); // Right SHIFT키가 눌렸을 경우 해제한다.

              // SHIFT+DEL를 INSERT키로 정의를 한다.
              register_code(KC_INS);
            } else {
            // DEL만 눌린 경우 DEL키로 정의한다.
            register_code(KC_DEL);
            }
          }
          // 키보드가 눌리지 않았을 경우 등록된 코드를 풀어준다.
          else
          {
            if (get_mods() & MOD_MASK_SHIFT)
            {
              unregister_code(KC_INS);
            } else {
              unregister_code(KC_DEL);
            }
          }
          return false; // 발생한 키 처리를 했으니 false 처리를 하여 중복으로 넘겨주지 않도록 한다.
#endif
  
        default:
          return true; // Process all other keycodes normally
      }
    return true;
};

bool muse_mode = false;
uint8_t last_muse_note = 0;
uint16_t muse_counter = 0;
uint8_t muse_offset = 70;
uint16_t muse_tempo = 50;

// wait DELAY ms before unregistering media keys
#define MEDIA_KEY_DELAY 10

void encoder_update_user(uint8_t index, bool clockwise) {
  uint16_t held_keycode_timer = timer_read();
  if (muse_mode) {
    if (IS_LAYER_ON(_RAISE)) {
      if (clockwise) {
        muse_offset++;
      } else {
        muse_offset--;
      }
    } else {
      if (clockwise) {
        muse_tempo+=1;
      } else {
        muse_tempo-=1;
      }
    }
  } else {
    if (clockwise) {
#if 0 // Page down/up
      register_code(KC_PGDN);
      unregister_code(KC_PGDN);
#else // Volume down/up
      register_code(KC_VOLU);
      while (timer_elapsed(held_keycode_timer) < MEDIA_KEY_DELAY) {
        // no-op
      }
      unregister_code(KC_VOLU);
#endif
    } else {
#if 0 // Page down/up
      register_code(KC_PGUP);
      unregister_code(KC_PGUP);
#else // volume down/up
      register_code(KC_VOLD);
      while (timer_elapsed(held_keycode_timer) < MEDIA_KEY_DELAY) {
        // no-op
      }
      unregister_code(KC_VOLD);
#endif
    }
  }
}

void dip_switch_update_user(uint8_t index, bool active) {
    switch (index) {
        case 0:
            if (active) {
                layer_on(_ADJUST);
            } else {
                layer_off(_ADJUST);
            }
            break;
        case 1:
            if (active) {
                muse_mode = true;
            } else {
                muse_mode = false;
            }
    }
}


void matrix_scan_user(void) {
#ifdef AUDIO_ENABLE
    if (muse_mode) {
        if (muse_counter == 0) {
            uint8_t muse_note = muse_offset + SCALE[muse_clock_pulse()];
            if (muse_note != last_muse_note) {
                stop_note(compute_freq_for_midi_note(last_muse_note));
                play_note(compute_freq_for_midi_note(muse_note), 0xF);
                last_muse_note = muse_note;
            }
        }
        muse_counter = (muse_counter + 1) % muse_tempo;
    } else {
        if (muse_counter) {
            stop_all_notes();
            muse_counter = 0;
        }
    }
#endif
}

bool music_mask_user(uint16_t keycode) {
  switch (keycode) {
    case RAISE:
    case LOWER:
      return false;
    default:
      return true;
  }
}
